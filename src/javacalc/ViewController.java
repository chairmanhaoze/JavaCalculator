    private void buttonMinusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonMinusActionPerformed
        // TODO add your handling code here:
        clicks = clicks + 1;
        if (clicks > 1) {
            secondNum = Double.parseDouble(calcDisplay.getText());
            if (operation == 1) {
                calculated = (double) model.addition(previous, secondNum);
                previous = Model.round(calculated, 5);
                previousNum.setText(Double.toString(previous) + "-");
                calcDisplay.setText("");
            }
            if (operation == 2) {
                calculated = (double) model.subtraction(previous, secondNum);
                previous = Model.round(calculated, 5);
                previousNum.setText(Double.toString(previous) + "-");
                calcDisplay.setText("");
            }
            if (operation == 3) {
                calculated = (double) model.multiplication(previous, secondNum);
                previous = Model.round(calculated, 5);
                previousNum.setText(Double.toString(previous) + "-");
                calcDisplay.setText("");
            }
            if (operation == 4) {
                calculated = (double) model.division(previous, secondNum);
                previous = Model.round(calculated, 5);
                previousNum.setText(Double.toString(previous) + "-");
                calcDisplay.setText("");
            }
            operation = 2;
        } else {
            operation = 2;
            previous = Double.parseDouble(calcDisplay.getText());
            previousNum.setText(previous + " -");
            calcDisplay.setText("");
        }
        negative = 0;
        buttonMinus.setEnabled(false);
    }//GEN-LAST:event_buttonMinusActionPerformed

    private void buttonEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEqualActionPerformed
        // TODO add your handling code here:
        secondNum = Double.parseDouble(calcDisplay.getText());
        previousNum.setText(Double.toString(secondNum));
        if (operation == 1) {
            calculated = (double) model.addition(previous, secondNum);
            calcDisplay.setText(Double.toString(Model.round(calculated, 5)));
        }
        if (operation == 2) {
            calculated = (double) model.subtraction(previous, secondNum);
            calcDisplay.setText(Double.toString(Model.round(calculated, 5)));
        }
        if (operation == 3) {
            calculated = (double) model.multiplication(previous, secondNum);
            calcDisplay.setText(Double.toString(Model.round(calculated, 5)));
        }
        if (operation == 4) {
            calculated = (double) model.division(previous, secondNum);
            calcDisplay.setText(Double.toString(Model.round(calculated, 5)));
        }
        negative = 0;
    }//GEN-LAST:event_buttonEqualActionPerformed

